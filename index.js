var dropDown = document.querySelector(".drop-down");
var profileDropDown = document.querySelector(".profile-drop-down");
var profileLookup = document.querySelector(".profile-lookup");


function dropDownToggle() {
    dropDown.classList.toggle("drop-down-height");
}
function profileDropDownToggle() {
    profileDropDown.classList.toggle("profile-drop-down-height")
}
function profileLookupToggle() {
    profileLookup.classList.toggle("profile-lookup-height")
}